function localizedTopic(text, key) {
  const rule = localizedTopicRules.find(([pattern]) => pattern.test(key));
  return text[rule?.[1] || "item"] || text.item;
}

function localizedDescriptor(text, key) {
  const leaf = key.split(".").at(-1) || "";
  if (/title/i.test(leaf)) return text.title;
  if (/description|detail|why|note/i.test(leaf)) return text.description;
  if (/status|state/i.test(leaf)) return text.status;
  if (/review|confirm|approval/i.test(leaf)) return text.review;
  if (/value|field|source|target|reference|command|mode|risk|gate/i.test(leaf)) return text.value;
  if (/current|selected|active/i.test(leaf)) return text.current;
  if (/next|open|action|operation|apply|change/i.test(leaf)) return text.next;
  if (/ready|passed|healthy|safe|synced|enabled|normal|compliant/i.test(leaf)) return text.ready;
  if (/missing|empty|unavailable|failed|blocked|warning|problem/i.test(leaf)) return text.missing;
  if (/unknown|custom|default|fallback/i.test(leaf)) return text.unknown;
  if (/page|menu/i.test(leaf)) return text.page;
  return text.item;
}

function localizedGeneratedMessage(code, key) {
  const text = localizedCatalog[code];
  if (!text) {
    return undefined;
  }
  return `${localizedTopic(text, key)}${text.separator}${localizedDescriptor(text, key)}`;
}

function messageKeys() {
  return Array.from(new Set([...Object.keys(baseMessages.en), ...Object.keys(baseMessages.ja)])).sort();
}

function buildMessages() {
  const keys = messageKeys();
  const result = {};
  for (const code of DASHBOARD_LOCALE_CODES) {
    const base = code === "ja" ? baseMessages.ja : code === "en" ? baseMessages.en : {};
    const overrides = commonLocaleOverrides[code] || {};
    result[code] = {};
    for (const key of keys) {
      result[code][key] = overrides[key] ?? base[key] ?? localizedGeneratedMessage(code, key);
      if (typeof result[code][key] !== "string") {
        throw new Error(`Missing Dashboard message for ${code}:${key}`);
      }
    }
    Object.freeze(result[code]);
  }
  return Object.freeze(result);
}

const messages = buildMessages();

export function dashboardMessageKeys() {
  return messageKeys();
}

export function dashboardMessagesForLocale(locale) {
  const code = normalizeDashboardLocale(locale, "en");
  return messages[code];
}

export function normalizeDashboardLocale(language, fallback = "en") {
  const value = String(language || "").trim();
  if (!value) {
    return fallback;
  }
  const normalized = value.replace("_", "-").toLowerCase();
  if (localeAliasMap.has(normalized)) {
    return localeAliasMap.get(normalized);
  }
  if (normalized.startsWith("zh-hant") || normalized.startsWith("zh-tw")) {
    return "zh-TW";
  }
  if (normalized.startsWith("zh")) {
    return "zh-CN";
  }
  if (normalized.startsWith("pt")) {
    return "pt-BR";
  }
  const languagePrefix = normalized.split("-")[0];
  return localeAliasMap.get(languagePrefix) || fallback;
}

export function getDashboardLocalePolicy(locale) {
  return localePolicyByCode.get(normalizeDashboardLocale(locale, "en")) || localePolicyByCode.get("en");
}

export function getDashboardLocaleDirection(locale) {
  return getDashboardLocalePolicy(locale).direction;
}

export function getDashboardIntlLocale(locale) {
  return getDashboardLocalePolicy(locale).intlLocale;
}

export function resolveLocale(languages = []) {
  const requested = Array.isArray(languages) && languages.length ? languages : ["en"];
  for (const language of requested) {
    const resolved = normalizeDashboardLocale(language, "");
    if (resolved) {
      return resolved;
    }
  }
  return "en";
}

export function createTranslator(locale) {
  const selected = normalizeDashboardLocale(locale, "en");
  return function translate(key, fallback = key) {
    return messages[selected][key] ?? fallback;
  };
}

export function formatDateTime(value, locale) {
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) {
    return value || "";
  }
  return new Intl.DateTimeFormat(locale, {
    dateStyle: "medium",
    timeStyle: "short",
  }).format(date);
}

export function formatRelativeAge(value, locale, now = new Date()) {
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) {
    return "";
  }
  const deltaSeconds = Math.round((date.getTime() - now.getTime()) / 1000);
  const absSeconds = Math.abs(deltaSeconds);
  const formatter = new Intl.RelativeTimeFormat(locale, { numeric: "auto" });
  if (absSeconds < 60) {
    return formatter.format(deltaSeconds, "second");
  }
  const deltaMinutes = Math.round(deltaSeconds / 60);
  if (Math.abs(deltaMinutes) < 60) {
    return formatter.format(deltaMinutes, "minute");
  }
  const deltaHours = Math.round(deltaMinutes / 60);
  if (Math.abs(deltaHours) < 24) {
    return formatter.format(deltaHours, "hour");
  }
  return formatter.format(Math.round(deltaHours / 24), "day");
}
